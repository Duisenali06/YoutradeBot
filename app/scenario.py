TOTAL_STEPS = 8


def get_step(step_num: int) -> dict | None:
    if step_num not in STEPS:
        return None
    step = STEPS[step_num].copy()
    step["text"] = format_step(step["text"])
    if step["buttons"]:
        step["buttons"] = [
            [(label, format_step(action)) for label, action in row]
            for row in step["buttons"]
        ]
    return step


