def render_push(push_id: str, urls: dict) -> dict | None:
    """Подставляем реальные URL в текст пуша."""
    for push in PUSH_SCHEDULE:
        if push["id"] == push_id:
            return {
                "id": push["id"],
                "text": push["text"].format(**urls),
                "media": push["media"],
            }
    return None


def get_push_by_index(index: int) -> dict | None:
    """Возвращает пуш по индексу (0-5)."""
    if 0 <= index < len(PUSH_SCHEDULE):
        return PUSH_SCHEDULE[index]
    return None


def get_schedule_count() -> int:
    return len(PUSH_SCHEDULE)
